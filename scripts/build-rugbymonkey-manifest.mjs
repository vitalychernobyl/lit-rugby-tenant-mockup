import { readFile, writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

import { archiveEvents } from "../rugbymonkey/public/data/archive-events-rm5.js";

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const appPath = join(root, "rugbymonkey/public/app-rm11.js");
const outputPath = join(root, "rugbymonkey/public/data/event-manifest-rm11.js");
const source = await readFile(appPath, "utf8");

function section(start, end) {
  const startIndex = source.indexOf(start);
  const endIndex = source.indexOf(end, startIndex);
  if (startIndex < 0 || endIndex < 0) throw new Error(`Unable to find ${start} section`);
  return source.slice(startIndex, endIndex);
}

function slugsFrom(block) {
  return [...block.matchAll(/\bslug:\s*"([a-z0-9-]+)"/g)].map((match) => match[1]);
}

const eventSlugs = [...new Set([
  ...slugsFrom(section("const currentEvents = [", "const profileEventEnrichments")),
  ...archiveEvents.map((event) => event.slug),
])].sort();
const teamSlugs = [...new Set(slugsFrom(section("const teams = [", "const people = [")))].sort();

const generated = `// Generated by scripts/build-rugbymonkey-manifest.mjs.\nexport const eventSlugs = ${JSON.stringify(eventSlugs, null, 2)};\n\nexport const teamSlugs = ${JSON.stringify(teamSlugs, null, 2)};\n`;

if (process.argv.includes("--check")) {
  const existing = await readFile(outputPath, "utf8").catch(() => "");
  if (existing !== generated) {
    console.error("Rugbymonkey event manifest is stale. Run npm run generate:rugbymonkey-manifest.");
    process.exitCode = 1;
  }
} else {
  await writeFile(outputPath, generated);
  console.log(`Generated ${eventSlugs.length} event routes and ${teamSlugs.length} team routes.`);
}
